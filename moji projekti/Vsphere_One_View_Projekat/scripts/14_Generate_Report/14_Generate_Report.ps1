function Invoke-ReportGeneration {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$Config,
        
        [Parameter(Mandatory=$false)]
        [ValidateSet("Summary", "Detailed", "Compliance", "Trends", "All")]
        [string]$ReportType = "All",
        
        [Parameter(Mandatory=$false)]
        [ValidateSet("HTML", "PDF", "JSON", "CSV", "Excel")]
        [string[]]$OutputFormat = @("HTML", "JSON"),
        
        [Parameter(Mandatory=$false)]
        [switch]$IncludeCharts = $true,
        
        [Parameter(Mandatory=$false)]
        [switch]$EmailReport = $false,
        
        [Parameter(Mandatory=$false)]
        [ValidateSet("Last24Hours", "LastWeek", "LastMonth", "Custom")]
        [string]$ReportPeriod = "Last24Hours",
        
        [Parameter(Mandatory=$false)]
        [datetime]$CustomStartDate,
        
        [Parameter(Mandatory=$false)]
        [datetime]$CustomEndDate
    )
    
    Try-CatchBlock -Context "Report Generation" -RecoveryActions @(
        (New-RecoveryAction -Action {
            param($ErrorInfo)
            Write-EnhancedLog "Pokušaj ponovnog generisanja izveštaja..." "INFO" "ReportGen"
            return @{ Success = $true }
        } -Name "Retry-ReportGeneration" -MaxAttempts 2)
    ) -FinallyBlock {
        Write-EnhancedLog "Završetak generisanja izveštaja..." "INFO" "ReportGen"
    } -ScriptBlock {
        
        Write-EnhancedLog "=== POČETAK GENERISANJA IZVEŠTAJA ===" "SUCCESS" "ReportGen"
        Write-EnhancedLog "Tip izveštaja: $ReportType" "INFO" "ReportGen"
        Write-EnhancedLog "Formati: $($OutputFormat -join ', ')" "INFO" "ReportGen"
        
        # Determine date range
        $dateRange = Get-ReportDateRange -Period $ReportPeriod -CustomStartDate $CustomStartDate -CustomEndDate $CustomEndDate
        
        $reportData = @()
        
        # Generate different report types
        if ($ReportType -in @("Summary", "All")) {
            $summaryData = Invoke-SummaryReportGeneration -Config $Config -DateRange $dateRange
            $reportData += @{ Type = "Summary"; Data = $summaryData }
            Write-EnhancedLog "Summary izveštaj generisan" "SUCCESS" "ReportGen"
        }
        
        if ($ReportType -in @("Detailed", "All")) {
            $detailedData = Invoke-DetailedReportGeneration -Config $Config -DateRange $dateRange
            $reportData += @{ Type = "Detailed"; Data = $detailedData }
            Write-EnhancedLog "Detailed izveštaj generisan" "SUCCESS" "ReportGen"
        }
        
        # Generate reports in different formats
        $generatedFiles = @()
        
        foreach ($format in $OutputFormat) {
            foreach ($report in $reportData) {
                $outputFile = "reports/$($report.Type)_Report_$(Get-Date -Format 'yyyyMMdd_HHmmss').$($format.ToLower())"
                
                switch ($format) {
                    "HTML" {
                        Generate-HTMLReport -ReportData $report.Data -OutputFile $outputFile -IncludeCharts:$IncludeCharts
                    }
                    "JSON" {
                        Export-Configuration -Config $report.Data -OutputFile $outputFile -Pretty
                    }
                }
                
                $generatedFiles += $outputFile
                Write-EnhancedLog "$($report.Type) izveštaj generisan kao $format: $outputFile" "SUCCESS" "ReportGen"
            }
        }
        
        # Create master report summary
        $masterReport = [PSCustomObject]@{
            ReportGenerationTime = Get-Date
            ReportTypes = $ReportType
            OutputFormats = $OutputFormat
            ReportPeriod = $ReportPeriod
            DateRange = $dateRange
            GeneratedFiles = $generatedFiles
            TotalFilesGenerated = $generatedFiles.Count
            ChartsIncluded = $IncludeCharts
            EmailSent = $EmailReport
            ReportData = $reportData
        }
        
        Write-EnhancedLog "=== GENERISANJE IZVEŠTAJA ZAVRŠENO ===" "SUCCESS" "ReportGen"
        Write-EnhancedLog "Ukupno fajlova generisano: $($generatedFiles.Count)" "INFO" "ReportGen"
        
        return $masterReport
    }
}

function Get-ReportDateRange {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$Period,
        
        [Parameter(Mandatory=$false)]
        [datetime]$CustomStartDate,
        
        [Parameter(Mandatory=$false)]
        [datetime]$CustomEndDate
    )
    
    $endDate = Get-Date
    $startDate = switch ($Period) {
        "Last24Hours" { $endDate.AddHours(-24) }
        "LastWeek" { $endDate.AddDays(-7) }
        "LastMonth" { $endDate.AddDays(-30) }
        "Custom" { 
            if (-not $CustomStartDate) { throw "CustomStartDate je obavezan za Custom period" }
            $CustomStartDate 
        }
        default { $endDate.AddHours(-24) }
    }
    
    if ($Period -eq "Custom" -and $CustomEndDate) {
        $endDate = $CustomEndDate
    }
    
    return [PSCustomObject]@{
        StartDate = $startDate
        EndDate = $endDate
        Period = $Period
    }
}

