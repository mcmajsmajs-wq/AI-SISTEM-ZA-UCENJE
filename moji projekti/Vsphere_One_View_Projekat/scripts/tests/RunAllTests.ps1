        default {
            throw "Unknown test type: $TestType"
        }
    }
    
    # Generate report and display summary
    $report = Generate-TestReport
    $overallSuccess = Display-TestSummary
    
    return $report
    
} catch {
    Write-TestLog "TEST RUNNER FAILED: $($_.Exception.Message)" "ERROR"
    Write-TestLog "Stack trace: $($_.ScriptStackTrace)" "DEBUG"
    exit 1
    
} finally {
    # Cleanup
    try {
        Write-TestLog "Performing test cleanup..." "INFO"
        
        # Clean up temporary files
        $tempFiles = Get-ChildItem "temp" -ErrorAction SilentlyContinue
        foreach ($file in $tempFiles) {
            try {
                Remove-Item $file.FullName -Recurse -Force
            } catch {
                Write-TestLog "Failed to cleanup temp file: $($file.Name)" "WARN"
            }
        }
        
        Write-TestLog "Test cleanup completed" "SUCCESS"
        
    } catch {
        Write-TestLog "Cleanup failed: $($_.Exception.Message)" "ERROR"
    }
    
    Write-TestLog "=== TEST RUNNER FINISHED ===" "SUCCESS"
}
