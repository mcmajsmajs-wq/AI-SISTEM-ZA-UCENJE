function Test-IntegrationTests {
    Write-TestLog "Running integration tests..." "INFO"
    
    # Test Configuration Integration
    Invoke-Test -TestName "Configuration Integration" -Category "Integration" -TestScript {
        $configFile = "config/settings.template.json"
        
        try {
            if (Test-Path $configFile) {
                # Load configuration
                $config = Get-Configuration -ConfigFile $configFile -SkipValidation
                
                # Test configuration merging
                $override = @{ system = @{ environment = "test" } }
                $merged = Merge-Configurations -BaseConfig $config -OverrideConfig $override
                
                $passed = ($merged.system.environment -eq "test")
                
                return @{
                    Passed = $passed
                    Message = if ($passed) { "Configuration integration working" } else { "Configuration integration failed" }
                    Details = @{ OriginalEnv = $config.system.environment; MergedEnv = $merged.system.environment }
                }
            } else {
                return @{ Passed = $false; Message = "Configuration file not found" }
            }
        } catch {
            return @{ Passed = $false; Message = "Configuration integration error: $($_.Exception.Message)" }
        }
    }
    
    # Test Logging Integration
    Invoke-Test -TestName "Logging Integration" -Category "Integration" -TestScript {
        $testLogFile = "temp/integration_test.log"
        
        try {
            # Ensure log directory exists
            $logDir = Split-Path $testLogFile -Parent
            if (-not (Test-Path $logDir)) {
                New-Item -Path $logDir -ItemType Directory -Force | Out-Null
            }
            
            # Test logging
            Write-EnhancedLog "Integration test message" "INFO" "IntegrationTest" -LogFile $testLogFile
            
            # Verify log entry
            if (Test-Path $testLogFile) {
                $logContent = Get-Content $testLogFile -Raw
                $passed = $logContent -contains "Integration test message"
                
                # Cleanup
                Remove-Item $testLogFile -Force
                
                return @{
                    Passed = $passed
                    Message = if ($passed) { "Logging integration working" } else { "Logging integration failed" }
                }
            } else {
                return @{ Passed = $false; Message = "Log file not created" }
            }
        } catch {
            return @{ Passed = $false; Message = "Logging integration error: $($_.Exception.Message)" }
        }
    }
    
    Write-TestLog "Integration tests completed" "INFO"
}

function Generate-TestReport {
    try {
        Write-TestLog "Generating test report..." "INFO"
        
        $endTime = Get-Date
        $totalDuration = ($endTime - $script:StartTime).TotalMinutes
        
        $passedTests = $script:TestResults | Where-Object { $_.Passed }
        $failedTests = $script:TestResults | Where-Object { -not $_.Passed }
        
        $report = [PSCustomObject]@{
            TestRunStartTime = $script:StartTime
            TestRunEndTime = $endTime
            TotalDurationMinutes = [math]::Round($totalDuration, 2)
            TestType = $TestType
            Simulation = $Simulation
            TotalTests = $script:TestResults.Count
            PassedTests = $passedTests.Count
            FailedTests = $failedTests.Count
            SuccessRate = if ($script:TestResults.Count -gt 0) { [math]::Round(($passedTests.Count / $script:TestResults.Count) * 100, 2) } else { 0 }
            OverallSuccess = ($failedTests.Count -eq 0)
            TestResults = $script:TestResults
            Categories = ($script:TestResults | Group-Object -Property Category | ForEach-Object { 
                @{
                    Category = $_.Name
                    Total = $_.Count
                    Passed = ($_.Group | Where-Object { $_.Passed }).Count
                    Failed = ($_.Group | Where-Object { -not $_.Passed }).Count
                }
            })
        }
        
        # Ensure reports directory exists
        if (-not (Test-Path "reports")) {
            New-Item -Path "reports" -ItemType Directory -Force | Out-Null
        }
        
        # Write report to file
        $reportJson = $report | ConvertTo-Json -Depth 10
        $reportJson | Out-File -FilePath $ReportFile -Encoding UTF8
        
        Write-TestLog "Test report saved to: $ReportFile" "SUCCESS"
        
        return $report
        
    } catch {
        Write-TestLog "Failed to generate test report: $($_.Exception.Message)" "ERROR"
        throw
    }
}

function Display-TestSummary {
    try {
        Write-TestLog "=== TEST SUMMARY ===" "SUCCESS"
        
        $endTime = Get-Date
        $duration = ($endTime - $script:StartTime).TotalMinutes
        
        Write-TestLog "Test execution time: $($duration.ToString('0.00')) minutes" "INFO"
        Write-TestLog "Test type: $TestType" "INFO"
        Write-TestLog "Total tests: $($script:TestResults.Count)" "INFO"
        
        $passedTests = $script:TestResults | Where-Object { $_.Passed }
        $failedTests = $script:TestResults | Where-Object { -not $_.Passed }
        
        Write-TestLog "Passed tests: $($passedTests.Count)" "SUCCESS"
        Write-TestLog "Failed tests: $($failedTests.Count)" if($failedTests.Count -gt 0) { "ERROR" } else { "SUCCESS" }
        
        if ($script:TestResults.Count -gt 0) {
            $successRate = [math]::Round(($passedTests.Count / $script:TestResults.Count) * 100, 2)
            Write-TestLog "Success rate: $successRate%" "INFO"
        }
        
        # Show failed tests if any
        if ($failedTests.Count -gt 0) {
            Write-TestLog "=== FAILED TESTS ===" "ERROR"
            foreach ($test in $failedTests) {
                Write-TestLog "FAILED: $($test.TestName) - $($test.Message)" "ERROR"
            }
        }
        
        $overallSuccess = ($failedTests.Count -eq 0)
        if ($overallSuccess) {
            Write-TestLog "=== ALL TESTS PASSED ===" "SUCCESS"
        } else {
            Write-TestLog "=== TESTS FAILED ===" "ERROR"
        }
        
        return $overallSuccess
        
    } catch {
        Write-TestLog "Failed to display test summary: $($_.Exception.Message)" "ERROR"
        return $false
    }
}

