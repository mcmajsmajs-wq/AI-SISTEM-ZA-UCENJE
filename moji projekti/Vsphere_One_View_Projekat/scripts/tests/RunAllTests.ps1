function Write-TestLog {
    param([string]$Message, [string]$Level = "INFO")
    Write-EnhancedLog $Message $Level $script:Component -LogFile $script:LogFile
}

function New-TestResult {
    param(
        [string]$TestName,
        [string]$Category,
        [bool]$Passed,
        [string]$Message = "",
        [timespan]$Duration = [timespan]::Zero,
        [hashtable]$Details = @{}
    )
    
    return [PSCustomObject]@{
        TestName = $TestName
        Category = $Category
        Passed = $Passed
        Message = $Message
        Duration = $Duration.TotalSeconds
        Timestamp = Get-Date
        Details = $Details
    }
}

function Invoke-Test {
    param(
        [string]$TestName,
        [string]$Category,
        [scriptblock]$TestScript,
        [hashtable]$Parameters = @{}
    )
    
    try {
        Write-TestLog "Running test: $TestName" "INFO"
        $testStart = Get-Date
        
        $result = & $TestScript @Parameters
        
        $testEnd = Get-Date
        $duration = $testEnd - $testStart
        
        if ($result -is [bool]) {
            $passed = $result
            $message = if ($passed) { "Test passed" } else { "Test failed" }
        } elseif ($result -is [hashtable] -and $result.ContainsKey('Passed')) {
            $passed = $result.Passed
            $message = if ($passed) { $result.Message } else { $result.Message }
        } else {
            $passed = $false
            $message = "Invalid test result format"
        }
        
        $testResult = New-TestResult -TestName $TestName -Category $Category -Passed $passed -Message $message -Duration $duration -Details $result
        $script:TestResults += $testResult
        
        $status = if ($passed) { "PASSED" } else { "FAILED" }
        Write-TestLog "Test $TestName`: $status" if($passed) { "SUCCESS" } else { "ERROR" }
        
        if ($Verbose) {
            Write-TestLog "Test result details: $($result | ConvertTo-Json -Compress)" "DEBUG"
        }
        
        return $testResult
        
    } catch {
        $testEnd = Get-Date
        $duration = $testEnd - $testStart
        
        $testResult = New-TestResult -TestName $TestName -Category $Category -Passed $false -Message "Test error: $($_.Exception.Message)" -Duration $duration
        $script:TestResults += $testResult
        
        Write-TestLog "Test $TestName`: ERROR - $($_.Exception.Message)" "ERROR"
        
        if ($Verbose) {
            Write-TestLog "Test error details: $($_.Exception.StackTrace)" "DEBUG"
        }
        
        return $testResult
    }
}

function Test-UnitTests {
    Write-TestLog "Running unit tests..." "INFO"
    
    # Test Logger functionality
    Invoke-Test -TestName "Logger Write-EnhancedLog" -Category "Unit" -TestScript {
        $testLogFile = "temp/test.log"
        $testMessage = "Test message"
        Write-EnhancedLog $testMessage "INFO" "TestUnit" -LogFile $testLogFile
        
        $fileContent = Get-Content $testLogFile -Raw
        $passed = $fileContent -contains $testMessage
        
        # Cleanup
        if (Test-Path $testLogFile) {
            Remove-Item $testLogFile -Force
        }
        
        return @{ Passed = $passed; Message = if ($passed) { "Log written successfully" } else { "Log not written" } }
    }
    
    # Test ConfigManager functionality
    Invoke-Test -TestName "ConfigManager Load Configuration" -Category "Unit" -TestScript {
        $testConfigFile = "config/settings.template.json"
        
        if (Test-Path $testConfigFile) {
            $config = Get-Configuration -ConfigFile $testConfigFile -SkipValidation
            $passed = ($null -ne $config -and $config.system.version -eq "1.0.0")
            
            return @{ 
                Passed = $passed; 
                Message = if ($passed) { "Configuration loaded successfully" } else { "Configuration not loaded" }
                Details = @{ Version = if ($config) { $config.system.version } else { $null } }
            }
        } else {
            return @{ Passed = $false; Message = "Test config file not found" }
        }
    }
    
    # Test ValidationEngine functionality
    Invoke-Test -TestName "ValidationEngine Parameter Validation" -Category "Unit" -TestScript {
        $params = @{
            TestParam = "TestValue"
            InvalidParam = $null
        }
        
        $schema = @{
            TestParam = @{ Type = 'string'; Required = $true }
            InvalidParam = @{ Type = 'string'; Required = $true }
        }
        
        $result = Test-Parameters -Parameters $params -Schema $schema
        $passed = (-not $result.IsValid) -and $result.Errors.Count -gt 0
        
        return @{
            Passed = $passed
            Message = if ($passed) { "Parameter validation working correctly" } else { "Parameter validation failed" }
            Details = @{ Errors = $result.Errors }
        }
    }
    
    # Test ErrorHandler functionality
    Invoke-Test -TestName "ErrorHandler Try-CatchBlock" -Category "Unit" -TestScript {
        $scriptExecuted = $false
        $errorCaught = $false
        
        $result = Try-CatchBlock -ScriptBlock {
            $script:scriptExecuted = $true
            throw "Test exception"
        } -Context "TestContext" -ContinueOnError -FinallyBlock {
            # Finally block should execute
        }
        
        $passed = $scriptExecuted -and ($null -eq $result)
        
        return @{
            Passed = $passed
            Message = if ($passed) { "Try-CatchBlock working correctly" } else { "Try-CatchBlock failed" }
        }
    }
    
    Write-TestLog "Unit tests completed" "INFO"
}

