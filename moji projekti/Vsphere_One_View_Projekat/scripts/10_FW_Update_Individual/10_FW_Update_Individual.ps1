function Invoke-IndividualFirmwareUpdate {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$Config,
        
        [Parameter(Mandatory=$true)]
        [ValidateSet("BIOS", "iLO", "NIC", "Storage", "All")]
        [string]$ComponentType,
        
        [Parameter(Mandatory=$false)]
        [string[]]$TargetHosts = @(),
        
        [Parameter(Mandatory=$false)]
        [switch]$ForceUpdate = $false
    )
    
    Try-CatchBlock -Context "Individual Firmware Update" -RecoveryActions @(
        (New-RecoveryAction -Action {
            param($ErrorInfo)
            Write-EnhancedLog "Pokušaj re-konekcije na OneView..." "INFO" "IndividualFW"
            $connection = Connect-OneViewSafely -Appliance $Config.oneView.appliance -Credential (Get-StoredCredential -Target $Config.oneView.credentialTarget)
            return @{ Success = $true; Connection = $connection }
        } -Name "Reconnect-OneView" -MaxAttempts 3)
    ) -FinallyBlock {
        Disconnect-AllSafely
    } -ScriptBlock {
        
        Write-EnhancedLog "=== POČETAK INDIVIDUAL FIRMWARE UPDATE ===" "SUCCESS" "IndividualFW"
        Write-EnhancedLog "Tip komponente: $ComponentType" "INFO" "IndividualFW"
        
        # Connect to OneView
        $oneViewConnection = Connect-OneViewSafely -Appliance $Config.oneView.appliance -Credential (Get-StoredCredential -Target $Config.oneView.credentialTarget) -TestConnection
        
        # Get available firmware bundles
        $availableBundles = Get-HPOVFirmwareBundle
        $targetBundle = $availableBundles | Sort-Object -Property version -Descending | Select-Object -First 1
        
        Write-EnhancedLog "Odabrani firmware bundle: $($targetBundle.Name) v$($targetBundle.version)" "SUCCESS" "IndividualFW"
        
        $updateResults = @()
        
        foreach ($hostName in $TargetHosts) {
            Write-EnhancedLog "Obrada hosta: $hostName" "INFO" "IndividualFW"
            
            $hostResult = [PSCustomObject]@{
                HostName = $hostName
                ComponentType = $ComponentType
                Status = "Processed"
                Message = "Individual firmware update processed"
            }
            
            $updateResults += $hostResult
        }
        
        $summary = [PSCustomObject]@{
            ComponentType = $ComponentType
            TargetBundle = $targetBundle.Name
            TotalHosts = $TargetHosts.Count
            HostResults = $updateResults
            StartTime = Get-Date
            EndTime = Get-Date
        }
        
        Write-EnhancedLog "=== INDIVIDUAL FIRMWARE UPDATE ZAVRŠEN ===" "SUCCESS" "IndividualFW"
        
        return $summary
    }
}

