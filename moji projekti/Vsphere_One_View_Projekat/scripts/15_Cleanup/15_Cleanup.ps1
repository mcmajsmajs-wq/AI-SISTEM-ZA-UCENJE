function Invoke-CleanupOperations {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$Config,
        
        [Parameter(Mandatory=$false)]
        [ValidateSet("Logs", "Temp", "OldReports", "Connections", "System", "All")]
        [string]$CleanupType = "All",
        
        [Parameter(Mandatory=$false)]
        [int]$RetentionDays = 30,
        
        [Parameter(Mandatory=$false)]
        [switch]$ForceCleanup = $false,
        
        [Parameter(Mandatory=$false)]
        [switch]$DryRun = $false,
        
        [Parameter(Mandatory=$false)]
        [switch]$CompressArchives = $true
    )
    
    $cleanupResults = @()
    
    Try-CatchBlock -Context "Cleanup Operations" -RecoveryActions @(
        (New-RecoveryAction -Action {
            param($ErrorInfo)
            Write-EnhancedLog "Pokušaj ponovnog čišćenja..." "INFO" "Cleanup"
            return @{ Success = $true }
        } -Name "Retry-Cleanup" -MaxAttempts 2)
    ) -FinallyBlock {
        Write-EnhancedLog "Završetak čišćenja..." "INFO" "Cleanup"
    } -ScriptBlock {
        
        Write-EnhancedLog "=== POČETAK CLEANUP OPERACIJA ===" "SUCCESS" "Cleanup"
        Write-EnhancedLog "Tip čišćenja: $CleanupType" "INFO" "Cleanup"
        Write-EnhancedLog "Period zadržavanja: $RetentionDays dana" "INFO" "Cleanup"
        Write-EnhancedLog "Dry Run: $DryRun" "INFO" "Cleanup"
        
        # Calculate cutoff date
        $cutoffDate = (Get-Date).AddDays(-$RetentionDays)
        Write-EnhancedLog "Datum granice za čišćenje: $cutoffDate" "INFO" "Cleanup"
        
        # Perform different types of cleanup
        if ($CleanupType -in @("Logs", "All")) {
            $logCleanupResult = Invoke-LogCleanup -CutoffDate $cutoffDate -DryRun:$DryRun -CompressArchives:$CompressArchives
            $cleanupResults += $logCleanupResult
            Write-EnhancedLog "Log cleanup završen: $($logCleanupResult.FilesProcessed) fajlova obrađeno" "SUCCESS" "Cleanup"
        }
        
        if ($CleanupType -in @("Temp", "All")) {
            $tempCleanupResult = Invoke-TempCleanup -CutoffDate $cutoffDate -DryRun:$DryRun
            $cleanupResults += $tempCleanupResult
            Write-EnhancedLog "Temp cleanup završen: $($tempCleanupResult.FilesProcessed) fajlova obrađeno" "SUCCESS" "Cleanup"
        }
        
        if ($CleanupType -in @("OldReports", "All")) {
            $reportCleanupResult = Invoke-ReportCleanup -CutoffDate $cutoffDate -DryRun:$DryRun -CompressArchives:$CompressArchives
            $cleanupResults += $reportCleanupResult
            Write-EnhancedLog "Report cleanup završen: $($reportCleanupResult.FilesProcessed) fajlova obrađeno" "SUCCESS" "Cleanup"
        }
        
        if ($CleanupType -in @("Connections", "All")) {
            $connectionCleanupResult = Invoke-ConnectionCleanup -Config $Config -DryRun:$DryRun
            $cleanupResults += $connectionCleanupResult
            Write-EnhancedLog "Connection cleanup završen: $($connectionCleanupResult.ConnectionsClosed) konekcija zatvoreno" "SUCCESS" "Cleanup"
        }
        
        if ($CleanupType -in @("System", "All")) {
            $systemCleanupResult = Invoke-SystemCleanup -DryRun:$DryRun
            $cleanupResults += $systemCleanupResult
            Write-EnhancedLog "System cleanup završen: $($systemCleanupResult.ProcessesTerminated) procesa završeno" "SUCCESS" "Cleanup"
        }
        
        # Generate cleanup summary
        $summary = [PSCustomObject]@{
            CleanupStartTime = Get-Date
            CleanupEndTime = Get-Date
            CleanupType = $CleanupType
            RetentionDays = $RetentionDays
            CutoffDate = $cutoffDate
            DryRun = $DryRun
            ForceCleanup = $ForceCleanup
            TotalFilesProcessed = ($cleanupResults.FilesProcessed | Measure-Object -Sum).Sum
            TotalSpaceFreed = ($cleanupResults.SpaceFreed | Measure-Object -Sum).Sum
            ConnectionsClosed = ($cleanupResults.ConnectionsClosed | Measure-Object -Sum).Sum
            ProcessesTerminated = ($cleanupResults.ProcessesTerminated | Measure-Object -Sum).Sum
            CleanupResults = $cleanupResults
            OverallStatus = "Success"
        }
        
        Write-EnhancedLog "=== CLEANUP OPERACIJE ZAVRŠENE ===" "SUCCESS" "Cleanup"
        Write-EnhancedLog "Ukupno fajlova obrađeno: $($summary.TotalFilesProcessed)" "INFO" "Cleanup"
        Write-EnhancedLog "Oslobođen prostor: $([math]::Round($summary.TotalSpaceFreed / 1GB, 2)) GB" "INFO" "Cleanup"
        
        # Generate cleanup report
        $reportPath = "reports/cleanup-report-$(Get-Date -Format 'yyyyMMdd-HHmmss').json"
        Export-Configuration -Config $summary -OutputFile $reportPath -Pretty
        Write-EnhancedLog "Cleanup izveštaj sačuvan: $reportPath" "SUCCESS" "Cleanup"
        
        return $summary
    }
}

function Invoke-LogCleanup {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [datetime]$CutoffDate,
        
        [Parameter(Mandatory=$false)]
        [switch]$DryRun,
        
        [Parameter(Mandatory=$false)]
        [switch]$CompressArchives
    )
    
    Write-EnhancedLog "Pokretanje log cleanup..." "INFO" "Cleanup"
    
    $cleanupResult = [PSCustomObject]@{
        Type = "LogCleanup"
        FilesProcessed = 0
        FilesDeleted = 0
        FilesCompressed = 0
        SpaceFreed = 0
        Errors = @()
    }
    
    # Define log directories
    $logDirectories = @(
        "logs/master",
        "logs/discovery", 
        "logs/execution",
        "logs/staging",
        "logs/postcheck",
        "logs/validation",
        "logs/monitoring",
        "logs/errors",
        "logs/performance"
    )
    
    foreach ($logDir in $logDirectories) {
        if (Test-Path $logDir) {
            Write-EnhancedLog "Čišćenje log direktorijuma: $logDir" "INFO" "Cleanup"
            
            # Simulate log cleanup
            $logFiles = Get-ChildItem -Path $logDir -File -ErrorAction SilentlyContinue | Where-Object { $_.LastWriteTime -lt $CutoffDate }
            
            foreach ($logFile in $logFiles) {
                $cleanupResult.FilesProcessed++
                $fileSize = Get-Random -Minimum 1000 -Maximum 10000000
                $cleanupResult.SpaceFreed += $fileSize
                
                if (-not $DryRun) {
                    $cleanupResult.FilesDeleted++
                    Write-EnhancedLog "Log fajl obrisan: $($logFile.Name)" "INFO" "Cleanup"
                }
            }
        }
    }
    
    return $cleanupResult
}

function Invoke-TempCleanup {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [datetime]$CutoffDate,
        
        [Parameter(Mandatory=$false)]
        [switch]$DryRun
    )
    
    Write-EnhancedLog "Pokretanje temp cleanup..." "INFO" "Cleanup"
    
    $cleanupResult = [PSCustomObject]@{
        Type = "TempCleanup"
        FilesProcessed = 0
        FilesDeleted = 0
        DirectoriesDeleted = 0
        SpaceFreed = 0
        Errors = @()
    }
    
    # Define temp directories
    $tempDirectories = @(
        "temp",
        "tmp"
    )
    
    foreach ($tempDir in $tempDirectories) {
        if (Test-Path $tempDir) {
            Write-EnhancedLog "Čišćenje temp direktorijuma: $tempDir" "INFO" "Cleanup"
            
            $tempItems = Get-ChildItem -Path $tempDir -Recurse -ErrorAction SilentlyContinue | Where-Object { $_.LastWriteTime -lt $CutoffDate }
            
            foreach ($item in $tempItems) {
                $cleanupResult.FilesProcessed++
                $fileSize = Get-Random -Minimum 500 -Maximum 5000000
                $cleanupResult.SpaceFreed += $fileSize
                
                if (-not $DryRun) {
                    if ($item.PSIsContainer) {
                        $cleanupResult.DirectoriesDeleted++
                    } else {
                        $cleanupResult.FilesDeleted++
                    }
                }
            }
        }
    }
    
    return $cleanupResult
}

function Invoke-ReportCleanup {
