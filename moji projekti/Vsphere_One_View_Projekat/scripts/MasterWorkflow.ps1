function Generate-WorkflowReport {
    try {
        Write-EnhancedLog "Generating workflow report..." "INFO" $script:Component
        
        $endTime = Get-Date
        $totalDuration = ($endTime - $script:StartTime).TotalMinutes
        
        $successfulPhases = $script:Results | Where-Object { $_.Success }
        $failedPhases = $script:Results | Where-Object { -not $_.Success }
        
        $report = [PSCustomObject]@{
            WorkflowStartTime = $script:StartTime
            WorkflowEndTime = $endTime
            TotalDurationMinutes = [math]::Round($totalDuration, 2)
            RequestedPhase = $Phase
            Simulation = $Simulation
            WhatIf = $WhatIf
            Environment = $Environment
            TotalPhases = $script:Results.Count
            SuccessfulPhases = $successfulPhases.Count
            FailedPhases = $failedPhases.Count
            OverallSuccess = ($failedPhases.Count -eq 0)
            PhaseResults = $script:Results
            SystemInfo = Get-SystemInfo
            EnvironmentInfo = Get-EnvironmentInfo
        }
        
        # Write report to file
        $reportFile = "reports/WorkflowReport_$(Get-Date -Format 'yyyyMMdd_HHmm').json"
        
        # Ensure reports directory exists
        if (-not (Test-Path "reports")) {
            New-Item -Path "reports" -ItemType Directory -Force | Out-Null
        }
        
        $report | ConvertTo-Json -Depth 10 | Out-File -FilePath $reportFile -Encoding UTF8
        
        Write-EnhancedLog "Workflow report saved to: $reportFile" "SUCCESS" $script:Component
        
        return $report
        
    } catch {
        Write-EnhancedLog "Failed to generate workflow report: $($_.Exception.Message)" "ERROR" $script:Component
        throw
    }
}

function Display-Summary {
    try {
        Write-EnhancedLog "=== WORKFLOW SUMMARY ===" "SUCCESS" $script:Component
        
        $endTime = Get-Date
        $duration = ($endTime - $script:StartTime).TotalMinutes
        
        Write-EnhancedLog "Total execution time: $($duration.ToString('0.00')) minutes" "INFO" $script:Component
        Write-EnhancedLog "Phase executed: $Phase" "INFO" $script:Component
        Write-EnhancedLog "Simulation mode: $Simulation" "INFO" $script:Component
        Write-EnhancedLog "Phases completed: $($script:Results.Count)" "INFO" $script:Component
        
        foreach ($result in $script:Results) {
            $status = if ($result.Success) { "SUCCESS" } else { "FAILED" }
            Write-EnhancedLog "Phase $($result.Phase): $status - $($result.Message)" "INFO" $script:Component
        }
        
        if (($script:Results | Where-Object { -not $_.Success }).Count -eq 0) {
            Write-EnhancedLog "=== WORKFLOW COMPLETED SUCCESSFULLY ===" "SUCCESS" $script:Component
        } else {
            Write-EnhancedLog "=== WORKFLOW COMPLETED WITH ERRORS ===" "ERROR" $script:Component
        }
        
    } catch {
        Write-EnhancedLog "Failed to display summary: $($_.Exception.Message)" "ERROR" $script:Component
    }
}

# Main execution
try {
    Initialize-Workflow
    
    # Load and validate configuration
    Load-Configuration
    Validate-Prerequisites
    
    # Execute requested phases
    switch ($Phase) {
        'Discovery' { 
            Invoke-DiscoveryPhase 
        }
        'Staging' { 
            Invoke-StagingPhase 
        }
        'Execution' { 
            Invoke-ExecutionPhase 
        }
        'PostCheck' { 
            Invoke-PostCheckPhase 
        }
        'All' { 
            Write-EnhancedLog "Executing all phases sequentially" "INFO" $script:Component
            Invoke-DiscoveryPhase
            Invoke-StagingPhase
            Invoke-ExecutionPhase
            Invoke-PostCheckPhase
        }
        default {
            throw "Unknown phase: $Phase"
        }
    }
    
    # Generate report and display summary
    $report = Generate-WorkflowReport
    Display-Summary
    
    return $report
    
} catch {
    Write-EnhancedLog "WORKFLOW FAILED: $($_.Exception.Message)" "ERROR" $script:Component
    Write-EnhancedLog "Stack trace: $($_.ScriptStackTrace)" "DEBUG" $script:Component
    
    try {
        Write-FatalErrorReport -ErrorInfo @{
            Timestamp = Get-Date
            Context = "MasterWorkflow"
            Exception = $_.Exception
            ErrorMessage = $_.Exception.Message
            Phase = $Phase
            Simulation = $Simulation
            WhatIf = $WhatIf
        }
    } catch {
        Write-EnhancedLog "Failed to write fatal error report" "ERROR" $script:Component
    }
    
    exit 1
    
} finally {
    # Cleanup
    try {
        Write-EnhancedLog "Performing workflow cleanup..." "INFO" $script:Component
        
        # Disconnect all connections safely
        Disconnect-AllSafely
        
        # Clear error history (keep recent entries)
        Clear-ErrorHistory -KeepLast 50
        
        # Generate final statistics
        $errorStats = Get-ErrorStatistics
        if ($errorStats.TotalErrors -gt 0) {
            Write-EnhancedLog "Error Statistics: $($errorStats.TotalErrors) errors, Recovery success rate: $($errorStats.RecoverySuccessRate)%" "INFO" $script:Component
        }
        
        Write-EnhancedLog "Workflow cleanup completed" "SUCCESS" $script:Component
        
    } catch {
        Write-EnhancedLog "Cleanup failed: $($_.Exception.Message)" "ERROR" $script:Component
    }
    
    Write-EnhancedLog "=== WORKFLOW FINISHED ===" "SUCCESS" $script:Component
}
