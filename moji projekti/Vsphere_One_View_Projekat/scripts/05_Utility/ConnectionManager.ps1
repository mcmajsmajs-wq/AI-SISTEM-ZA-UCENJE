function Connect-vCenterSafely {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$Server,
        
        [Parameter(Mandatory=$true)]
        [PSCredential]$Credential,
        
        [Parameter(Mandatory=$false)]
        [int]$TimeoutSeconds = 30,
        
        [Parameter(Mandatory=$false)]
        [switch]$TestConnection,
        
        [Parameter(Mandatory=$false)]
        [int]$MaxRetries = 3,
        
        [Parameter(Mandatory=$false)]
        [int]$RetryDelaySeconds = 10
    )
    
    $attempt = 0
    $lastError = $null
    
    while ($attempt -lt $MaxRetries) {
        $attempt++
        
        try {
            Write-EnhancedLog "Connecting to vCenter: $Server (attempt $attempt/$MaxRetries)" "INFO" "vCenter"
            
            # Check server connectivity first
            if (-not (Test-NetConnection -ComputerName $Server -Port 443 -WarningAction SilentlyContinue).TcpTestSucceeded) {
                throw "Network connectivity test to $Server:443 failed"
            }
            
            # Connect to vCenter
            $connection = Connect-VIServer -Server $Server -Credential $Credential -TimeoutSec $TimeoutSeconds -ErrorAction Stop
            
            if (-not $connection) {
                throw "Connection returned null"
            }
            
            Write-EnhancedLog "Successfully connected to vCenter: $Server" "SUCCESS" "vCenter"
            
            # Perform health check if requested
            if ($TestConnection) {
                $healthResult = Test-vCenterConnection -Connection $connection
                if (-not $healthResult.Success) {
                    throw "Connection health check failed: $($healthResult.Message)"
                }
                Write-EnhancedLog "vCenter connection health check passed" "SUCCESS" "vCenter"
            }
            
            # Store connection for cleanup
            $script:vCenterConnections = @($script:vCenterConnections) + $connection
            
            return $connection
            
        } catch {
            $lastError = $_
            Write-EnhancedLog "vCenter connection attempt $attempt failed: $($_.Exception.Message)" "ERROR" "vCenter"
            
            if ($attempt -lt $MaxRetries) {
                Write-EnhancedLog "Retrying in $RetryDelaySeconds seconds..." "WARN" "vCenter"
                Start-Sleep -Seconds $RetryDelaySeconds
            }
        }
    }
    
    # All retries failed
    $finalError = "Failed to connect to vCenter after $MaxRetries attempts. Last error: $($lastError.Exception.Message)"
    Write-EnhancedLog $finalError "ERROR" "vCenter"
    throw $finalError
}

function Test-vCenterConnection {
    [CmdletBinding()]
    param($Connection)
    
    try {
        Write-EnhancedLog "Performing vCenter connection health check" "INFO" "vCenter"
        
        # Test basic operations
        $datacenter = Get-Datacenter -Server $Connection -ErrorAction Stop | Select-Object -First 1
        if (-not $datacenter) {
            throw "Unable to retrieve Datacenter objects"
        }
        
        # Test VM operations
        $testVM = Get-VM -Server $Connection -ErrorAction Stop | Select-Object -First 1
        Write-EnhancedLog "Datacenter: $($datacenter.Name), Sample VM: $($testVM.Name)" "DEBUG" "vCenter"
        
        # Test service instance
        $serviceInstance = Get-View ServiceInstance -Server $Connection -ErrorAction Stop
        if (-not $serviceInstance) {
            throw "Unable to access ServiceInstance"
        }
        
        # Check API version
        $apiVersion = $serviceInstance.Content.About.Version
        Write-EnhancedLog "vCenter API version: $apiVersion" "INFO" "vCenter"
        
        return [PSCustomObject]@{ 
            Success = $true 
            Message = "Connection healthy" 
            Datacenter = $datacenter.Name
            APIVersion = $apiVersion
        }
        
    } catch {
        $errorMsg = "vCenter health check failed: $($_.Exception.Message)"
        Write-EnhancedLog $errorMsg "ERROR" "vCenter"
        return [PSCustomObject]@{ 
            Success = $false 
            Message = $errorMsg 
        }
    }
}

function Connect-OneViewSafely {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$Appliance,
        
        [Parameter(Mandatory=$false)]
        [PSCredential]$Credential,
        
        [Parameter(Mandatory=$false)]
        [string]$Username,
        
        [Parameter(Mandatory=$false)]
        [string]$Password,
        
        [Parameter(Mandatory=$false)]
        [int]$TimeoutSeconds = 30,
        
        [Parameter(Mandatory=$false)]
        [switch]$TestConnection,
        
        [Parameter(Mandatory=$false)]
        [int]$MaxRetries = 3,
        
        [Parameter(Mandatory=$false)]
        [int]$RetryDelaySeconds = 10
    )
    
    # Handle credential parameters
    if (-not $Credential -and $Username -and $Password) {
        $securePassword = ConvertTo-SecureString -String $Password -AsPlainText -Force
        $Credential = New-Object System.Management.Automation.PSCredential($Username, $securePassword)
    }
    
    if (-not $Credential) {
        throw "Either Credential parameter or Username/Password parameters must be provided"
    }
    
    $attempt = 0
    $lastError = $null
    
    while ($attempt -lt $MaxRetries) {
        $attempt++
        
        try {
            Write-EnhancedLog "Connecting to OneView: $Appliance (attempt $attempt/$MaxRetries)" "INFO" "OneView"
            
            # Check server connectivity first
            if (-not (Test-NetConnection -ComputerName $Appliance -Port 443 -WarningAction SilentlyContinue).TcpTestSucceeded) {
                throw "Network connectivity test to $Appliance:443 failed"
            }
            
            # Connect to OneView
            $connection = Connect-HPOVResource -Appliance $Appliance -UserName $Credential.UserName -Password $Credential.GetNetworkCredential().Password -ErrorAction Stop
            
            if (-not $connection) {
                throw "Connection returned null"
            }
            
            Write-EnhancedLog "Successfully connected to OneView: $Appliance" "SUCCESS" "OneView"
            
            # Perform health check if requested
            if ($TestConnection) {
                $healthResult = Test-OneViewConnection -Connection $connection
                if (-not $healthResult.Success) {
                    throw "Connection health check failed: $($healthResult.Message)"
                }
                Write-EnhancedLog "OneView connection health check passed" "SUCCESS" "OneView"
            }
            
            # Store connection for cleanup
            $script:oneViewConnections = @($script:oneViewConnections) + $connection
            
            return $connection
            
        } catch {
            $lastError = $_
            Write-EnhancedLog "OneView connection attempt $attempt failed: $($_.Exception.Message)" "ERROR" "OneView"
            
            if ($attempt -lt $MaxRetries) {
                Write-EnhancedLog "Retrying in $RetryDelaySeconds seconds..." "WARN" "OneView"
                Start-Sleep -Seconds $RetryDelaySeconds
            }
        }
    }
    
    # All retries failed
    $finalError = "Failed to connect to OneView after $MaxRetries attempts. Last error: $($lastError.Exception.Message)"
    Write-EnhancedLog $finalError "ERROR" "OneView"
    throw $finalError
}

