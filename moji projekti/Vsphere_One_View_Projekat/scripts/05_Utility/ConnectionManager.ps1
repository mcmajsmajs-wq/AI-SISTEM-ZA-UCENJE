function Disconnect-AllSafely {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$false)]
        [switch]$Force = $false
    )
    
    try {
        Write-EnhancedLog "Disconnecting all connections..." "INFO" "ConnectionManager"
        
        # Disconnect vCenter connections
        if ($script:vCenterConnections) {
            foreach ($connection in $script:vCenterConnections) {
                if ($connection.IsConnected) {
                    try {
                        Write-EnhancedLog "Disconnecting from vCenter: $($connection.Name)" "INFO" "vCenter"
                        Disconnect-VIServer -Server $connection -Confirm:$false -ErrorAction SilentlyContinue
                    } catch {
                        Write-EnhancedLog "Error disconnecting from vCenter: $($_.Exception.Message)" "WARN" "vCenter"
                    }
                }
            }
            $script:vCenterConnections = @()
        }
        
        # Disconnect OneView connections
        if ($script:oneViewConnections) {
            foreach ($connection in $script:oneViewConnections) {
                try {
                    Write-EnhancedLog "Disconnecting from OneView" "INFO" "OneView"
                    Disconnect-HPOVResource -ErrorAction SilentlyContinue
                } catch {
                    Write-EnhancedLog "Error disconnecting from OneView: $($_.Exception.Message)" "WARN" "OneView"
                }
            }
            $script:oneViewConnections = @()
        }
        
        Write-EnhancedLog "All connections disconnected successfully" "SUCCESS" "ConnectionManager"
        
    } catch {
        Write-EnhancedLog "Error during disconnection: $($_.Exception.Message)" "ERROR" "ConnectionManager"
        if ($Force) {
            # Force disconnect by clearing connection variables
            $script:vCenterConnections = @()
            $script:oneViewConnections = @()
            Write-EnhancedLog "Forced disconnection completed" "WARN" "ConnectionManager"
        } else {
            throw
        }
    }
}

function Test-SystemConnectivity {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string[]]$Targets,
        
        [Parameter(Mandatory=$false)]
        [int]$Port = 443,
        
        [Parameter(Mandatory=$false)]
        [int]$TimeoutMs = 5000
    )
    
    $results = @()
    
    foreach ($target in $Targets) {
        try {
            Write-EnhancedLog "Testing connectivity to $target`:$Port" "INFO" "ConnectionManager"
            
            $testResult = Test-NetConnection -ComputerName $target -Port $Port -WarningAction SilentlyContinue
            
            $result = [PSCustomObject]@{
                Target = $target
                Port = $Port
                TcpTestSucceeded = $testResult.TcpTestSucceeded
                PingSucceeded = $testResult.PingSucceeded
                NameResolveSucceeded = $testResult.NameResolutionSucceeded
                InterfaceAlias = $testResult.InterfaceAlias
                SourceAddress = $testResult.SourceAddress
                PingReplyDetails = $testResult.PingReplyDetails
                Timestamp = Get-Date
            }
            
            if ($testResult.TcpTestSucceeded) {
                Write-EnhancedLog "Connectivity test to $target`:$Port: SUCCESS" "SUCCESS" "ConnectionManager"
            } else {
                Write-EnhancedLog "Connectivity test to $target`:$Port: FAILED" "ERROR" "ConnectionManager"
            }
            
            $results += $result
            
        } catch {
            Write-EnhancedLog "Error testing connectivity to $target: $($_.Exception.Message)" "ERROR" "ConnectionManager"
            $results += [PSCustomObject]@{
                Target = $target
                Port = $Port
                TcpTestSucceeded = $false
                Error = $_.Exception.Message
                Timestamp = Get-Date
            }
        }
    }
    
    return $results
}

