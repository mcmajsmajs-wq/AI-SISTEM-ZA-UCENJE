function Get-WindowsCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName
    )
    
    try {
        # Try to get credential from Windows Credential Manager
        $credential = Get-StoredCredential -Target $TargetName -ErrorAction SilentlyContinue
        
        if ($credential) {
            return $credential
        } else {
            throw "Windows credential not found: $TargetName"
        }
        
    } catch {
        throw "Windows credential retrieval failed: $($_.Exception.Message)"
    }
}

function Set-WindowsCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName,
        
        [Parameter(Mandatory=$true)]
        [PSCredential]$Credential,
        
        [Parameter(Mandatory=$false)]
        [string]$Description
    )
    
    try {
        # Store credential in Windows Credential Manager
        $params = @{
            Target = $TargetName
            Credential = $Credential
            Persist = "LocalMachine"
        }
        
        if ($Description) {
            $params.Comment = $Description
        }
        
        New-StoredCredential @params -ErrorAction Stop
        
    } catch {
        throw "Windows credential storage failed: $($_.Exception.Message)"
    }
}

