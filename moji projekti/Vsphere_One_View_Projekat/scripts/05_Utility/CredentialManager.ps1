        $credentialPath = Join-Path $StorePath "$TargetName.cred"
        $usernamePath = Join-Path $StorePath "$TargetName.user"
        
        if (Test-Path $credentialPath) {
            Remove-Item $credentialPath -Force
        }
        
        if (Test-Path $usernamePath) {
            Remove-Item $usernamePath -Force
        }
        
    } catch {
        throw "File credential removal failed: $($_.Exception.Message)"
    }
}

function Get-EnvironmentCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName
    )
    
    try {
        $username = Get-Content "env:${TargetName}_USERNAME" -ErrorAction SilentlyContinue
        $password = Get-Content "env:${TargetName}_PASSWORD" -ErrorAction SilentlyContinue
        
        if (-not $username -or -not $password) {
            throw "Environment variables not found for: $TargetName"
        }
        
        $securePassword = ConvertTo-SecureString -String $password -AsPlainText -Force
        $credential = New-Object System.Management.Automation.PSCredential($username, $securePassword)
        
        return $credential
        
    } catch {
        throw "Environment credential retrieval failed: $($_.Exception.Message)"
    }
}

