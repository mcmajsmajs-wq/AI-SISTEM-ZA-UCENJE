function Test-CredentialAvailability {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName,
        
        [Parameter(Mandatory=$false)]
        [ValidateSet("Windows", "File", "Environment")]
        [string]$CredentialStore = "Windows",
        
        [Parameter(Mandatory=$false)]
        [string]$StorePath = "config/credentials"
    )
    
    try {
        switch ($CredentialStore) {
            "Windows" {
                $credential = Get-StoredCredential -Target $TargetName -ErrorAction SilentlyContinue
                return $null -ne $credential
            }
            "File" {
                $credentialPath = Join-Path $StorePath "$TargetName.cred"
                return Test-Path $credentialPath
            }
            "Environment" {
                $username = Get-Content "env:${TargetName}_USERNAME" -ErrorAction SilentlyContinue
                $password = Get-Content "env:${TargetName}_PASSWORD" -ErrorAction SilentlyContinue
                return ($null -ne $username -and $null -ne $password)
            }
        }
        
        return $false
        
    } catch {
        Write-EnhancedLog "Error testing credential availability: $($_.Exception.Message)" "ERROR" "CredentialManager"
        return $false
    }
}

function Get-CredentialStoreInfo {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$false)]
        [string]$StorePath = "config/credentials"
    )
    
    try {
        $stores = @()
        
        # Check Windows Credential Manager
        try {
            $windowsCreds = Get-StoredCredential -ErrorAction SilentlyContinue
            $stores += [PSCustomObject]@{
                StoreType = "Windows"
                Available = $true
                CredentialCount = if ($windowsCreds) { 1 } else { 0 }
                Path = "Windows Credential Manager"
            }
        } catch {
            $stores += [PSCustomObject]@{
                StoreType = "Windows"
                Available = $false
                CredentialCount = 0
                Path = "Windows Credential Manager"
                Error = $_.Exception.Message
            }
        }
        
        # Check File store
        $fileStoreAvailable = Test-Path $StorePath
        $fileCredentials = if ($fileStoreAvailable) { 
            Get-ChildItem $StorePath -Filter "*.cred" | Measure-Object | Select-Object -ExpandProperty Count
        } else { 
            0 
        }
        
        $stores += [PSCustomObject]@{
            StoreType = "File"
            Available = $fileStoreAvailable
            CredentialCount = $fileCredentials
            Path = $StorePath
        }
        
        # Check Environment store
        $envVars = Get-ChildItem env:* | Where-Object { $_.Name -match "_USERNAME$|_PASSWORD$" }
        $stores += [PSCustomObject]@{
            StoreType = "Environment"
            Available = $true
            CredentialCount = [math]::Floor($envVars.Count / 2)
            Path = "Environment Variables"
        }
        
        return $stores
        
    } catch {
        Write-EnhancedLog "Error getting credential store info: $($_.Exception.Message)" "ERROR" "CredentialManager"
        return @()
    }
}

