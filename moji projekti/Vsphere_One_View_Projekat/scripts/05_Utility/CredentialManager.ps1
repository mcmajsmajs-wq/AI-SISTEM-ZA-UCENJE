function Get-SecureCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName,
        
        [Parameter(Mandatory=$false)]
        [ValidateSet("Windows", "File", "Environment")]
        [string]$CredentialStore = "Windows",
        
        [Parameter(Mandatory=$false)]
        [string]$StorePath = "config/credentials"
    )
    
    try {
        Write-EnhancedLog "Retrieving credential: $TargetName from $CredentialStore store" "INFO" "CredentialManager"
        
        $credential = switch ($CredentialStore) {
            "Windows" {
                Get-WindowsCredential -TargetName $TargetName
            }
            "File" {
                Get-FileCredential -TargetName $TargetName -StorePath $StorePath
            }
            "Environment" {
                Get-EnvironmentCredential -TargetName $TargetName
            }
        }
        
        if ($credential) {
            Write-EnhancedLog "Credential retrieved successfully: $TargetName" "SUCCESS" "CredentialManager"
        } else {
            throw "Credential not found: $TargetName"
        }
        
        return $credential
        
    } catch {
        Write-EnhancedLog "Failed to retrieve credential $TargetName: $($_.Exception.Message)" "ERROR" "CredentialManager"
        throw
    }
}

