function Get-FileCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName,
        
        [Parameter(Mandatory=$false)]
        [string]$StorePath = "config/credentials"
    )
    
    try {
        $credentialPath = Join-Path $StorePath "$TargetName.cred"
        
        if (-not (Test-Path $credentialPath)) {
            throw "File credential not found: $credentialPath"
        }
        
        # Read encrypted password
        $secureString = Get-Content $credentialPath | ConvertTo-SecureString -ErrorAction Stop
        
        # Get username from separate file (for security)
        $usernamePath = Join-Path $StorePath "$TargetName.user"
        $username = if (Test-Path $usernamePath) { 
            Get-Content $usernamePath 
        } else { 
            # Fallback to target name
            $TargetName 
        }
        
        # Create credential object
        $credential = New-Object System.Management.Automation.PSCredential($username, $secureString)
        
        return $credential
        
    } catch {
        throw "File credential retrieval failed: $($_.Exception.Message)"
    }
}

function Set-FileCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$TargetName,
        
        [Parameter(Mandatory=$true)]
        [PSCredential]$Credential,
        
        [Parameter(Mandatory=$false)]
        [string]$StorePath = "config/credentials"
    )
    
    try {
        # Ensure store directory exists
        if (-not (Test-Path $StorePath)) {
            New-Item -Path $StorePath -ItemType Directory -Force | Out-Null
        }
        
        $credentialPath = Join-Path $StorePath "$TargetName.cred"
        $usernamePath = Join-Path $StorePath "$TargetName.user"
        
        # Store encrypted password
        $Credential.Password | ConvertFrom-SecureString | Out-File -FilePath $credentialPath -Encoding UTF8
        
        # Store username separately (for better security)
        $Credential.UserName | Out-File -FilePath $usernamePath -Encoding UTF8
        
    } catch {
        throw "File credential storage failed: $($_.Exception.Message)"
    }
}

function Remove-FileCredential {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
