function Test-ConfigurationValidity {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$Config
    )
    
    try {
        $errors = @()
        
        # System section validation
        if ($Config.system) {
            if ($Config.system.environment -notin @("development", "staging", "production", "disaster-recovery")) {
                $errors += "Invalid environment: $($Config.system.environment)"
            }
            
            if ($Config.system.timeoutMinutes -and $Config.system.timeoutMinutes -lt 1) {
                $errors += "Timeout minutes must be greater than 0"
            }
        }
        
        # vCenter section validation
        if ($Config.vCenter) {
            if (-not $Config.vCenter.server) {
                $errors += "vCenter server name is required"
            }
            
            if ($Config.vCenter.port -and ($Config.vCenter.port -lt 1 -or $Config.vCenter.port -gt 65535)) {
                $errors += "vCenter port must be between 1 and 65535"
            }
        }
        
        # OneView section validation
        if ($Config.oneView) {
            if (-not $Config.oneView.appliance) {
                $errors += "OneView appliance name is required"
            }
            
            if ($Config.oneView.timeoutSeconds -and $Config.oneView.timeoutSeconds -lt 1) {
                $errors += "OneView timeout seconds must be greater than 0"
            }
        }
        
        # Execution section validation
        if ($Config.execution) {
            if ($Config.execution.parallelHosts -and $Config.execution.parallelHosts -lt 1) {
                $errors += "Parallel hosts must be at least 1"
            }
            
            if ($Config.execution.maintenanceModeTimeout -and $Config.execution.maintenanceModeTimeout -lt 1) {
                $errors += "Maintenance mode timeout must be greater than 0"
            }
        }
        
        # Safety section validation
        if ($Config.safety) {
            if ($Config.safety.heartbeatFailureThreshold -and $Config.safety.heartbeatFailureThreshold -lt 1) {
                $errors += "Heartbeat failure threshold must be at least 1"
            }
            
            if ($Config.safety.rollbackTimeout -and $Config.safety.rollbackTimeout -lt 1) {
                $errors += "Rollback timeout must be greater than 0"
            }
        }
        
        return [PSCustomObject]@{
            IsValid = ($errors.Count -eq 0)
            Errors = $errors
        }
        
    } catch {
        Write-EnhancedLog "Configuration validation error: $($_.Exception.Message)" "ERROR" "ConfigManager"
        return [PSCustomObject]@{
            IsValid = $false
            Errors = @("Configuration validation failed: $($_.Exception.Message)")
        }
    }
}

function Merge-Configurations {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$BaseConfig,
        
        [Parameter(Mandatory=$true)]
        [object]$OverrideConfig
    )
    
    try {
        # Create a deep copy of base config
        $mergedConfig = $BaseConfig | ConvertTo-Json -Depth 100 | ConvertFrom-Json
        
        # Recursively merge configurations
        Merge-Object -Target $mergedConfig -Source $OverrideConfig
        
        Write-EnhancedLog "Configuration merge completed successfully" "SUCCESS" "ConfigManager"
        return $mergedConfig
        
    } catch {
        Write-EnhancedLog "Configuration merge failed: $($_.Exception.Message)" "ERROR" "ConfigManager"
        throw
    }
}

function Merge-Object {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$Target,
        
        [Parameter(Mandatory=$true)]
        [object]$Source
    )
    
    foreach ($property in $Source.PSObject.Properties) {
        if ($property.Value -is [PSCustomObject] -and $Target.PSObject.Properties.Name -contains $property.Name) {
            # Recursively merge nested objects
            Merge-Object -Target $Target.$($property.Name) -Source $property.Value
        } else {
            # Override or add property
            $Target | Add-Member -NotePropertyName $property.Name -NotePropertyValue $property.Value -Force
        }
    }
}

function Export-Configuration {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [object]$Config,
        
        [Parameter(Mandatory=$true)]
        [string]$OutputFile,
        
        [Parameter(Mandatory=$false)]
        [switch]$Pretty
    )
    
    try {
        Write-EnhancedLog "Exporting configuration to: $OutputFile" "INFO" "ConfigManager"
        
        # Ensure output directory exists
        $outputDir = Split-Path $OutputFile -Parent
        if (-not (Test-Path $outputDir)) {
            New-Item -Path $outputDir -ItemType Directory -Force | Out-Null
        }
        
        # Convert to JSON
        if ($Pretty) {
            $json = $Config | ConvertTo-Json -Depth 100
        } else {
            $json = $Config | ConvertTo-Json -Depth 100 -Compress
        }
        
        # Write to file
        $json | Out-File -FilePath $OutputFile -Encoding UTF8
        
        Write-EnhancedLog "Configuration exported successfully" "SUCCESS" "ConfigManager"
        
    } catch {
        Write-EnhancedLog "Failed to export configuration: $($_.Exception.Message)" "ERROR" "ConfigManager"
        throw
    }
}

