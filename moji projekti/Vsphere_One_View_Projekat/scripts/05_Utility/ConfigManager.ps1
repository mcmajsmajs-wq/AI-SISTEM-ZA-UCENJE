function Get-Configuration {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory=$true)]
        [string]$ConfigFile,
        
        [Parameter(Mandatory=$false)]
        [string]$SchemaFile,
        
        [Parameter(Mandatory=$false)]
        [string]$Environment,
        
        [Parameter(Mandatory=$false)]
        [switch]$SkipValidation
    )
    
    try {
        Write-EnhancedLog "Loading configuration from: $ConfigFile" "INFO" "ConfigManager"
        
        # Check if config file exists
        if (-not (Test-Path $ConfigFile)) {
            throw "Configuration file not found: $ConfigFile"
        }
        
        # Load base configuration
        $configJson = Get-Content $ConfigFile -Raw
        $config = $configJson | ConvertFrom-Json
        
        # Add metadata
        $config | Add-Member -NotePropertyName "configFile" -NotePropertyValue $ConfigFile
        $config | Add-Member -NotePropertyName "configDirectory" -NotePropertyValue (Split-Path $ConfigFile -Parent)
        
        # Load environment-specific configuration if specified
        if ($Environment) {
            $envConfigFile = Join-Path (Split-Path $ConfigFile -Parent) "environments\$Environment.json"
            if (Test-Path $envConfigFile) {
                Write-EnhancedLog "Loading environment configuration from: $envConfigFile" "INFO" "ConfigManager"
                $envConfigJson = Get-Content $envConfigFile -Raw
                $envConfig = $envConfigJson | ConvertFrom-Json
                
                # Merge configurations (environment overrides base)
                $config = Merge-Configurations -BaseConfig $config -OverrideConfig $envConfig
            } else {
                Write-EnhancedLog "Environment config file not found: $envConfigFile" "WARN" "ConfigManager"
            }
        }
        
        # Validate configuration if not skipped
        if (-not $SkipValidation) {
            if ($SchemaFile) {
                $validationResult = Test-ConfigurationSchema -Config $config -SchemaFile $SchemaFile
                if (-not $validationResult.IsValid) {
                    throw "Configuration schema validation failed: $($validationResult.Errors -join ', ')"
                }
                Write-EnhancedLog "Configuration schema validation passed" "SUCCESS" "ConfigManager"
            } else {
                # Default schema validation
                $validationResult = Test-ConfigurationValidity -Config $config
                if (-not $validationResult.IsValid) {
                    throw "Configuration validation failed: $($validationResult.Errors -join ', ')"
                }
                Write-EnhancedLog "Configuration validation passed" "SUCCESS" "ConfigManager"
            }
        }
        
        Write-EnhancedLog "Configuration loaded successfully for environment: $($config.system.environment)" "SUCCESS" "ConfigManager"
        return $config
        
    } catch {
        Write-EnhancedLog "Failed to load configuration: $($_.Exception.Message)" "ERROR" "ConfigManager"
        throw
    }
}

